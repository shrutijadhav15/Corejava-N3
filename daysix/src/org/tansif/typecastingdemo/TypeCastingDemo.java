package org.tansif.typecastingdemo;

public class TypeCastingDemo {
	
//programe to demonstrate on typecasting
	
	public static void main(String[] args) {
		
		// implicit typecasting
		
		
		int x=15;
	    float y=x;
	    System.out.println("The value Y is:"+y);
	    
	    // explicit typecasting
	    
	    double d=12.5;  
         long l=(long)d;
         System.out.println("The value l is:"+l);
         
         
	}

}
